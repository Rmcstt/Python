lista = ['a', 'b', 'c', 'd', 'e', 'f', 'g']
print(lista[0:3])  # ['a', 'b', 'c']
print(lista[:3])   # ['a', 'b', 'c']
print(lista[3:])  # ['d', 'e', 'f', 'g']
print(lista[::2])  # ['d', 'e', 'f', 'g']
print(lista[::-1])  # ['g', 'f', 'e', 'd', 'c', 'b', 'a']